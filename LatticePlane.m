coords=MapThread[If[#1,#2,DimensionReduction[#2,2][#2,"ReconstructedData"]]&,{TF,coords}];
{d,order}=(FindShortestTour[#]&/@coords)\[Transpose];
\[ScriptCapitalP]int=MapThread[Polygon[#1[[#2[[;;-2]]]]]&,{coords,order}]
]


